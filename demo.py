print(10**3)     # 幂运算 1000
print(100 / 3)   # 精确除 33.3333
# 地板除  一正一负向下取整
print(100 // 3)  # 33
print(100 // -3)  # -34
print(-100 // 3)  # -34
# 取余数 = 被除数 - 除数 * 商
print(100 % 3)  # 1
print(100 % -3)  # -2 = 100 - (-3) * (-34)
print(-100 % 3)  # 2 = -100 - 3 * (-34)
print(100 % 7)
print(-100 % 7)  # -100-7*(-15)
